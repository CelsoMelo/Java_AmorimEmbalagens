    private void jButtonEditDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditDoneActionPerformed
        // TODO add your handling code here:
        ProdutoCRUD crd = new ProdutoCRUD();
        int col = tableProd.getSelectedRow();
        int codProd = (int) tableProd.getValueAt(col, 0);
        String descProd = jTextFieldEditDesc.getText();
        double quantProd = Double.parseDouble(jTextFieldEditQuant.getText());
        String unidade = jComboBoxUnidadeEdit.getSelectedItem().toString();
        double precProd = Double.parseDouble(jTextFieldEditPrec.getText());
        
        if(unidade.equalsIgnoreCase("Unitário")) {
            unidade = "Un";
        }
        
        if(crd.editar(codProd, descProd, quantProd,unidade, precProd) == 1) {
            jLabelStatus.setForeground(Color.BLUE);
            jLabelStatus.setText("Produto Editado Com Sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao editar Produto!");
            jLabelStatus.setText("");
        }
        jTextFieldEditDesc.setEnabled(false);
        jTextFieldEditQuant.setEnabled(false);
        jTextFieldEditPrec.setEnabled(false);
        jButtonEditDone.setEnabled(false);
        jButtonEditCancel.setEnabled(false);
        jComboBoxUnidadeEdit.setEnabled(false);
        readJTable();
    }//GEN-LAST:event_jButtonEditDoneActionPerformed

