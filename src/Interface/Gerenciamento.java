    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        // TODO add your handling code here:
        
        Produto p = new Produto();
        ProdutoCRUD crd = new ProdutoCRUD();
        
        if(txtProdName.getText().equals("") || txtProdQuant.getText().equals("") || txtProdPrec.getText().equals("")) {
            jLabelStatus.setForeground(Color.red);
            jLabelStatus.setText("Preencha todos os campos!");
        } else if (!(txtProdName.getText().equals("") || txtProdQuant.getText().equals("") || txtProdPrec.getText().equals(""))){
            p.setDesc(txtProdName.getText());
            p.setQuant(Double.parseDouble(txtProdQuant.getText()));
            if(jComboBoxUnidade.getSelectedItem().toString().equalsIgnoreCase("Kg")) {
                p.setUnidade("Kg");
            } else if(jComboBoxUnidade.getSelectedItem().toString().equalsIgnoreCase("Unitário")) {
                p.setUnidade("Un");
            }
            p.setPrecAtac(Double.parseDouble(txtProdPrec.getText()));
            
            crd.add(p);
            
            txtProdName.setText("");
            txtProdQuant.setText("");
            txtProdPrec.setText("");
            jLabelStatus.setForeground(Color.green);
            jLabelStatus.setText("Produto adicionado com sucesso!");
        }
        
        readJTable();
    }//GEN-LAST:event_jButtonAddActionPerformed

    private void jButtonRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverActionPerformed
        // TODO add your handling code here:
        ProdutoCRUD crd = new ProdutoCRUD();       
        int col = tableProd.getSelectedRow();
        int codProd = (int) tableProd.getValueAt(col, 0);
        
        if(crd.remover(codProd) == 1) {
            jLabelStatus.setForeground(Color.green);
            jLabelStatus.setText("Produto Removido com Sucesso!");
        } else if (crd.remover(codProd) == 2) {
            jLabelStatus.setForeground(Color.red);
            jLabelStatus.setText("Erro ao Remover Produto");
        }
        
        readJTable();
    }//GEN-LAST:event_jButtonRemoverActionPerformed

    private void tableProdMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableProdMouseClicked
        // TODO add your handling code here:
        int col = tableProd.getSelectedRow();
        int codProd = (int) tableProd.getValueAt(col, 0);
        String  prod = (String) tableProd.getValueAt(col, 1);
        double quant = (double) tableProd.getValueAt(col, 2);
        String unidade = (String) tableProd.getValueAt(col, 3);
        double prec = (double) tableProd.getValueAt(col, 4);
        jLabelProduto.setText("Produto: " + prod);
        jLabelCodProd.setText("Código: " + codProd);
        jTextFieldEditDesc.setText(prod);
        jTextFieldEditQuant.setText(Double.toString(quant));
        jTextFieldEditPrec.setText(Double.toString(prec));
        if(unidade.equalsIgnoreCase("Kg")) {
            jComboBoxUnidadeEdit.setSelectedIndex(0);
        } else if(unidade.equalsIgnoreCase("Un")) {
            jComboBoxUnidadeEdit.setSelectedIndex(1);
        }

        jButtonRemover.setEnabled(true);
        jButtonEdit.setEnabled(true);
      
    }//GEN-LAST:event_tableProdMouseClicked

