    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) tableProd.getModel();
        modelo.setNumRows(0);
        
        ProdutoCRUD crd = new ProdutoCRUD();
        
        for(Produto p: crd.read()) {
            
            modelo.addRow(new Object[]{
                p.getCod(),
                p.getDesc(),
                p.getQuant(),
                p.getUnidade(),
                p.getPrecAtac()
            });
            
        }
    }

