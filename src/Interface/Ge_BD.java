    private void jButtonResetAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonResetAllActionPerformed
        // TODO add your handling code here:
        CompraCRUD comp = new CompraCRUD();
        ProdutoCRUD prod = new ProdutoCRUD();
        ItemCompraCRUD item =  new ItemCompraCRUD();
        
        int r = JOptionPane.showConfirmDialog(null, "Cuidado! Você está prestes a resetar\n "
                + "os dados de todas as tabelas!\n Essa ação não poderá ser revertida.\n "
                + "Deseja Continuar?", "Confirmar", 
                JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE);
        
        if(r == JOptionPane.YES_OPTION) {
            comp.resetar();
            prod.resetar();
            item.resetar();
            JOptionPane.showMessageDialog(null, "Todas as tabelas foram resetadas com sucesso!");
        }
        attDados();
    }//GEN-LAST:event_jButtonResetAllActionPerformed


