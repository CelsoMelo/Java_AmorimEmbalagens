    public void attDados() {
        CompraCRUD comp = new CompraCRUD();
        int tComp = comp.getQuantDados();
        String qtC = Integer.toString(tComp);
        jTextFieldVend.setText(qtC);
        
        ItemCompraCRUD item = new ItemCompraCRUD();
        int tIt = item.getQuantDados();
        String qtIt = Integer.toString(tIt);
        jTextFieldItem.setText(qtIt);
        
        int tEst = item.getQuantDadosEstats();
        String qtEstat = Integer.toString(tEst);
        jTextFieldEstats.setText(qtEstat);
        
        ProdutoCRUD prod = new ProdutoCRUD();
        int tProd = prod.getQuantDados();
        String qtProd = Integer.toString(tProd);
        jTextFieldProd.setText(qtProd);
        
        int total = tComp + tIt + tEst + tProd;
        String qTotal = Integer.toString(total);
        jTextFieldTotal.setText(qTotal);
    }

