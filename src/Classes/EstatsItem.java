public class EstatsItem {
    
    private int codItem;
    private String nomeProd;
    private double quantItem;
    private String unidItem;
    private double precItem;
    private double vlrTot;
    private double lucro;

    public int getCodItem() {
        return codItem;
    }

    public void setCodItem(int codItem) {
        this.codItem = codItem;
    }

    public String getNomeProd() {
        return nomeProd;
    }

    public void setNomeProd(String nomeProd) {
        this.nomeProd = nomeProd;
    }

    public double getQuantItem() {
        return quantItem;
    }

    public void setQuantItem(double quantItem) {
        this.quantItem = quantItem;
    }

    public String getUnidItem() {
        return unidItem;
    }

    public void setUnidItem(String unidItem) {
        this.unidItem = unidItem;
    }

    public double getPrecItem() {
        return precItem;
    }

    public void setPrecItem(double precItem) {
        this.precItem = precItem;
    }

    public double getVlrTot() {
        return vlrTot;
    }

    public void setVlrTot(double vlrTot) {
        this.vlrTot = vlrTot;
    }

    public double getLucro() {
        return lucro;
    }

    public void setLucro(double lucro) {
        this.lucro = lucro;
    }
    
    
    
}
